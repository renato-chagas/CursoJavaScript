let num = 9.54432;

console.log(math.floor(num)); // Retorna 9, arrendonda para baixo.

console.log(math.ceil(num)); // Retorna 10, arrendonda para cima.

console.log(math.round(num)); // Retorna 10, arrendonda para o mais próximo.

console.log(math.max(1, 2, 3, 4, 5)); // Retorna 5, retorna o maior número.

console.log(math.min(1, 2, 3, 4, 5)); // Retorna 1, retorna o menor número.

console.log(math.random()); // Retorna um número aleatório entre 0 e 1.

console.log(math.random() * 100); // Retorna um número aleatório entre 0 e 100.

console.log(!!(100/0)); // Retorna true, pois é um número infinito.